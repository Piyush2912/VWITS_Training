package collections1;

import java.util.LinkedList;
import java.util.Queue;

public class LinkedListString {
	public static void main(String argsp[]) {
		Queue<String> q = new LinkedList<String>();
		
		q.add("Piyush");
		q.add("John");
		q.add("Ram");
		q.remove();
		
		System.out.println(q.peek());
		
		
		q.peek();
		
		
	}
}
